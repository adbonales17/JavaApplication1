    private class BotonActionListener implements ActionListener {
        @Override
        public void actionPerformed(ActionEvent evt) {
            JButton boton = (JButton) evt.getSource();
            Color color = boton.getBackground();

            JButton nuevoBoton = new JButton();
            nuevoBoton.setBackground(color);
            nuevoBoton.setText(getColorName(color));
            panel.add(nuevoBoton);
            botones.add(nuevoBoton);
            panel.updateUI();

            // Agregar el mismo ActionListener al nuevo botón generado
            nuevoBoton.addActionListener(this);
        }
    }
