    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JButton boton = new JButton();
        Color randomColor = generateRandomColor();
        
        while (randomColor.equals(ultimoColorAgregado)) {
        randomColor = generateRandomColor(); // Genera un nuevo color si es igual al último color agregado
        }
        
        boton.setBackground(randomColor);
        boton.setText(getColorName(randomColor));
        panel.add(boton);
        botones.add(boton);
        indice++;
        panel.updateUI();
        
        // Crear una instancia del ActionListener para los botones generados
        BotonActionListener actionListener = new BotonActionListener();

        // Agregar ActionListener al nuevo botón generado
        boton.addActionListener(actionListener);
        
        ultimoColorAgregado = randomColor; // Almacena el último color agregado
    }//GEN-LAST:event_jButton1ActionPerformed
    
    private Color generateRandomColor(){
        int randomIndex = (int) (Math.random() * COLORES.length);
        return COLORES[randomIndex];
    }
    
    private String getColorName(Color color) {
        if (color.equals(Color.RED)){
            return "Rojo";
        } else if (color.equals(Color.GREEN)){
            return "Verde";
        } else if (color.equals(Color.BLUE)){
            return "Azul";
        } else if (color.equals(Color.YELLOW)) {
            return "Amarillo";
        }else {
            return "Custom Color";
        }    
    }
